export function trackById(id) { return curriculumTracks.find((track) => track.id === id) || curriculumTracks[0]; }
export function trackForLesson(lessonId) { return curriculumTracks.find((track) => track.lessonIds.includes(lessonId)) || curriculumTracks[0]; }
export function lessonsForTrack(track, lessons) { const byId = new Map(lessons.map((lesson) => [lesson.id, lesson])); return track.lessonIds.map((id) => byId.get(id)).filter(Boolean); }
export function evaluateLessonSource(lessonId, code) {
  const checks = requiredSyntax[lessonId] || [{ label: "Write a non-empty NovaDev solution", pattern: /\S{8,}/ }];
  return checks.map((check) => ({ label: check.label, passed: check.pattern.test(code) }));
}
